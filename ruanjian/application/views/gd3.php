                                            <BR>&nbsp;&nbsp;&nbsp;1931年9月18日，中国人民永远难忘的一天。这天晚上10时20分，日本关东军独立守备队第二大队第三中队所属的河本末守中尉率其部下，经过长时间密谋和精心策划，炸毁了沈阳北郊柳条湖附近的一段南满铁路，栽赃嫁祸于中国守军。以此为借口，日本关东军向中国军队发起猛烈攻击，炮轰沈阳北大营和兵工厂，这就是震惊中外的“九一八事变”。
                                            <BR>&nbsp;&nbsp;&nbsp;日本对中国东北垂涎已久，把侵占东北作为其吞并中国、称霸亚洲及太平洋地区的首要战略目标。1927年夏，日本政府强化对华侵略政策。田中内阁在东京召开“东方会议”，制定了图谋侵占“满蒙”的“根本政策”，抛出了臭名昭著的“田中奏折”，露骨地声称中国东北对日本的生存有着“重大的利害关系”，公然宣称“惟欲征服中国，必先征服满蒙；如欲征服世界，必先征服中国。”
                                            <BR>&nbsp;&nbsp;&nbsp;九一八事变的发生，是日本侵占中国野心的大暴露。纪录片将这场悲壮而伟大的战争中所锻造的中华民族万众一心、同仇敌忾、坚忍不拔、威武不屈、前赴后继、坚持到底的民族精神展现出来，同时也揭示了这场付出了巨大民族牺牲而换来胜利的深刻意义，作为每一个有血有肉的中国人都不应该忘记也不允许忘记！
                                            <BR>&nbsp;&nbsp;&nbsp;此次观影活动极大地增强了党员的爱国意识，激发了党员的爱国热情，全体党员一致表示要牢记历史，深切反思，坚守职责，不辱使命，勿忘国殇，为实现中华民族伟大复兴的目标而全力奋斗！
