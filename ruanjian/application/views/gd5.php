                                    <SPAN style="FONT-SIZE: 10.5pt; FONT-FAMILY: 宋体; BACKGROUND: rgb(255,255,255); COLOR: rgb(0,0,0); mso-font-kerning: 0.0000pt; mso-shading: rgb(255,255,255)">
                                        <SPAN style="FONT-SIZE: 10.5pt; FONT-FAMILY: 宋体; mso-font-kerning: 0.0000pt">
                                            <BR>&nbsp;&nbsp;&nbsp;11月26日上午八点，2018全国大学生电子商务“创新、创意及创业”挑战赛东北林业大学校内选拔赛在我校成栋楼拉开帷幕。
                                            <BR>&nbsp;&nbsp;&nbsp;本次校内选拔赛历时两个月，覆盖面广，专业性强，赛事是由教育部高等学校电子商务类专业教学指导委员会主办的，采取主题赛方式，注重从创新思维、创意设计和创业实施三个方面来评价作品。组委会通过线上、线下多种方式积极宣传动员，得到了全校同学的热烈响应，共收到来自信息学院、经管学院、工程学院、土工学院、交通学院、理学院、林学院、文法学院、园林学院、材料学院、野生动物学院、外语学院等12个学院300名同学的63个参赛作品。
                                            <BR>&nbsp;&nbsp;&nbsp;开幕式开始后，主持人戈晓楠一一介绍了到场嘉宾与评委老师们。随后，由董本志副院长致辞，董院长对比赛的组织情况进行了详细的介绍，同时表达了信息学院对承办本次活动的支持，希望同学们通过这场比赛能够有所收获，有所积累，有所提升。致辞结束，主持人介绍了大赛评选规则和一些注意事项，并宣布比赛开始。
                                            <BR>&nbsp;&nbsp;&nbsp;本次比赛规模为历年来此项赛事我省各高校校赛中规模最大的一次，甚至超过了往年省赛的规模。赛事组委会对赛事规则、奖项设置以及答辩流程等环节都做了精心的安排和演练，邀请了来自哈尔滨商业大学、哈尔滨理工大学、知名电商企业以及东林五个学院的12位专家构成了评委组，比赛分为3组同时进行，各团队基于创业策划书和创业成果按照小组抽签的顺序进行限时汇报与答辩，经过耐心、细致、专业的评审最终评选出获奖团队30支，其中特等奖3支、一等奖6支、二等奖9支、三等奖12支，完成了一项公平、精彩、高效、有益的比赛。
                                            <BR>&nbsp;&nbsp;&nbsp;闭幕式中，来自哈尔滨理工大学连续承办两次省赛的杨彩霞老师对此次赛事进行了点评，负责本项赛事的来自信息学院的纪明宇老师对本次校赛进行了总结。
                                            <BR>&nbsp;&nbsp;&nbsp;本次校内选拔赛由信息与计算机工程学院承办，赛事获得了教务处组织的2017年度“学院杯”学科竞赛项目的大力资助，在此向教务处的鼎力支持表示最衷心的感谢。
