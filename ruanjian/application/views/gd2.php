                                    <SPAN style="FONT-SIZE: 10.5pt; FONT-FAMILY: 宋体; BACKGROUND: rgb(255,255,255); COLOR: rgb(0,0,0); mso-font-kerning: 0.0000pt; mso-shading: rgb(255,255,255)">
                                        <SPAN style="FONT-SIZE: 10.5pt; FONT-FAMILY: 宋体; mso-font-kerning: 0.0000pt">
                                            <BR>&nbsp;&nbsp;&nbsp;为庆祝党的十九大的顺利召开，响应学校和学院党委的号召，为进一步提高信息学院研究生环境卫生意识，创建安全整洁、文明、和谐、舒适的学习环境，信息与计算机工程学院研究生开展了“喜迎十九大，青春在行动”优秀实验室评比活动。
                                            <BR>&nbsp;&nbsp;&nbsp;在活动准备阶段，研究生实验室提交实验室打扫卫生前后对比照片进行活动报名。2017年10月30日上午10时，学院研究生党支部全体支委对实验室进行逐一检查，重点对实验室卫生、用电安全以及防火、防盗等方面进行了检查，对发现的问题现场提出意见并予以整改。学院一直高度重视研究生实验室安全，在检查过程中强调进入实验室切记：“随手关闭电源，随手倾倒垃圾”；从细处入手，共同营造一个安全卫生的学习环境；作为研究生要时刻紧绷安全这根弦，时刻不放松、不松懈，做到早预防、早准备、早发现、早处理，将安全隐患消灭在萌芽状态，杜绝研究生实验室安全卫生隐患，保证学院科研工作的顺利进行。
                                            <BR>&nbsp;&nbsp;&nbsp;本次评比中，根据实验室物品摆放是否整齐，垃圾是否及时清理，室内是否干净整洁，在规定时间内是否提交报名照片等方面综合评比，最后评比出：一等奖为：936、911；二等奖为 917；三等奖为946、921。
                                            <BR>&nbsp;&nbsp;&nbsp;通过此次活动，引导研究生投身实践，从身边的事情做起，凝聚青春力量有干劲，在实现中国梦的生动实践中放飞青春梦想，在为人民利益的不懈奋斗中书写人生的新华章。实验室评比督促研究生养成良好的卫生和生活习惯，提升安全意识，努力营造了一个安全，健康、舒适的学习、科研环境，共同建设安全和谐的美丽校园，用实际行动庆祝党的十九大顺利召开。
