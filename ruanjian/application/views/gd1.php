                                            <BR>&nbsp;&nbsp;&nbsp;ACM国际大学生程序设计竞赛（ACM International Collegiate Programming Contest, 简称ACM-ICPC或ICPC）是由国际计算机界具有悠久历史的权威性组织(美国)计算机协会（Association for Computing Machinery, 简称ACM）主办的一项旨在展示大学生创新能力、团队精神和在压力下编写程序、分析和解决问题能力的年度竞赛。目前，ACM国际大学生程序设计竞赛被誉为IT行业“奥林匹克”，在业内被高度认可，获得良好比赛成绩的学生得到了国内外高校和各著名科技公司的高度认可，为其读取研究生和进入谷歌、 脸书、百度、腾讯、阿里巴巴等高科技公司工作提供了敲门砖。
                                            <BR>&nbsp;&nbsp;&nbsp;2017年11月4日至5日，第42届ACM国际大学生程序设计竞赛亚洲区域赛（青岛）在中国石油大学（华东）体育馆举行。来自中国内地、中国香港地区和朝鲜的225所高校（国内所有985院校和211院校全部都参加）和1所中学的365支参赛队1095名参赛者共同争夺2018年世界总决赛出线资格，参赛高校、参赛队伍和参赛人数均创ACM有史以来最高纪录。经过角逐，最终产生金牌36项，银牌72项，铜牌108项，我校代表队在本次亚洲赛中共荣获2枚银牌和1枚铜牌，这是我校在该项赛事中荣获的第3枚和第4枚银牌（2013年和2014年各获得一枚银牌），时隔2年后又延续了能在亚洲赛中摘银的佳绩。
