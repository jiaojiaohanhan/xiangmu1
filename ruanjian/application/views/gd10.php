                                    <SPAN style="FONT-SIZE: 10.5pt; FONT-FAMILY: 宋体; BACKGROUND: rgb(255,255,255); COLOR: rgb(0,0,0); mso-font-kerning: 0.0000pt; mso-shading: rgb(255,255,255)">
                                        <SPAN style="FONT-SIZE: 10.5pt; FONT-FAMILY: 宋体; mso-font-kerning: 0.0000pt">
                                            <BR>&nbsp;&nbsp;&nbsp;为庆祝党的十九大胜利召开，引导学生成长成才，提升学院研究生就业质量，2017年11月8日12时，信息学院在丹青楼201开展了“中国梦 不忘初心 牢记使命”职业生涯工作坊。会议由信息学院研究生第一党支部书记李婧坤主持，出席此次活动的有信息学院研究生全体党员、积极分子。
                                            <BR>&nbsp;&nbsp;&nbsp;中国梦是我们这一代的，更是青年一代的。中国梦赋予当代大学生新的历史责任。作为实现中国梦的重要后备力量，青年大学生应响应时代号召，切实担负起实现中华民族伟大复兴的历史重任，要将个人理想融入实现中国梦的共同理想中，将个人梦与创业梦、成才梦结合起来，努力学习，掌握本领，提高创新能力，努力成为创造型人才。首先，支部书记李婧坤领学了十九大报告相关内容。之后，研究生党员代表李晓霜、郑雪分别就中国梦谈了自己的理解。学硕党员代表李晓霜同学认为中国梦是现代梦、自强梦，作为信息学院的研究生应该努力学习专业知识，认真研究国内外先进技术，在人工智能研究领域，为提高中国科技的核心竞争力而不懈努力；专硕党员代表郑雪强调了科技创新的重要性，认为要成为科技创新型人才，需要加强实践锻炼。接下来，李婧坤阐述了对中国梦的理解：不忘初心跟党走，我将积极引导研究生的思想及成长，不忘初心，牢记使命，从小我做起，以饱满的精神，优质的服务，坚持以线上线下相结合的教育方式，向国家输出优秀的信息人才而不断努力。
                                            <BR>&nbsp;&nbsp;&nbsp;接下来，李婧坤老师从2017届毕业生去向、就业协议签订的注意事项、档案发送流程、研究生求职诚信和职业生涯规划教育等方面进行了讲述，要求学生树立职业生涯规划意识，尽早了解自己和未来工作世界，提高自身综合素质，评估、权衡多方面信息，进行决策并付诸行动，不断在求职、就业的过程中进行再评估和探索，不断修正自己的生涯规划。李老师讲解全面细致、生动形象，同学们学到了职业生涯规划和就业指导方面的相关知识。最后李老师给研究生提了四点建议：简历制作需尽早，提交材料要准确，贪黑熬夜要适度，求职就业早行动。最后号召全体研究生：迎难而上、开拓进取、与时俱进、攻坚克难，努力学习成才, 共同携手用火红的青春浇筑美丽的中国梦。
                                            <BR>&nbsp;&nbsp;&nbsp;本次活动圆满结束，站在新的历史起点上，作为信息学子将提高自己的综合素质，提升自身的专业技能，将齐心协力完成好研究生的各项工作；党的十九大将为党、国家和人民描绘新的蓝图，信息学子将一如既往地坚定信念，牢记使命，敢于担当，率先垂范，做好每项工作，认真科研，为中国梦贡献个人的力量！
