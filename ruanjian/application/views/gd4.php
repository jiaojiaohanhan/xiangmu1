                                            <BR>&nbsp;&nbsp;&nbsp;11月29日下午，李顺龙副校长为信息与计算机工程学院全体党员上了一堂生动的主题党课。党课由计算机科学与技术系党支部书记李林辉主持。
                                            <BR>&nbsp;&nbsp;&nbsp;李顺龙副校长以《新时代生态文明观与低碳发展——建设生态文明，共创美丽中国》为题，解读了十九大报告内容，通过对十九大报告中关于“生态文明”“美丽”“绿色”的阐述，明确了建设生态文明，共创美丽中国的主题。
                                            <BR>&nbsp;&nbsp;&nbsp;李顺龙副校长认真践行新时代生态文明观、人类社会面临的最艰难课题、实现可持续发展的必经之路、中国已启动低碳发展新模式、注重林业对低碳发展的贡献五个方面，阐述了新时代生态文明观的必要性、全球气候变化严重性、低碳发展对于解决气候变化问题的重要性、我国对低碳发展新模式的探索和努力以及林业在低碳发展的重要作用。
                                            <BR>&nbsp;&nbsp;&nbsp;李顺龙副校长强调，要建设美丽中国，发展低碳经济是一个重要举措，应建立健全低碳循环发展的经济体系，倡导广大党员采取简约适度、绿色低碳的生活方式，反对奢侈浪费和不合理消费。李顺龙副校长指出，生态文明建设功在当代、利在千秋，我们要牢固树立社会主义生态文明观，推动形成人与自然和谐发展的现代化建设新格局，要结合学校特色为保护生态环境做出贡献。
                                            <BR>&nbsp;&nbsp;&nbsp; 李顺龙副校长用通俗易懂的语言、引人入胜的案例，深入浅出地讲述了“新时代生态文明观与低碳发展”的密切关系，为广大党员构建富强民主和谐美丽中国指明了努力方向。
